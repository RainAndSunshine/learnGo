package main

import "fmt"

func main() {
	//5:	101
	//2:	010

	//&:按位与（两位均为1才为1）
	//101
	//010
	fmt.Println(5 & 2)

	//|:按位或（两位有一个为1就为1）
	//101
	//010
	fmt.Println(5 | 2)

	//^:按位异或（两位不一样则为1）
	//101
	//010
	fmt.Println(5 ^ 2)

	//<<:将二进制位左移指定位数
	//101
	//1010
	fmt.Println(5 << 1)

	//>>:将二进制位右移指定位数
	//101
	//10
	fmt.Println(5 >> 1)
	//注意移位之后不要越界，int8最多是八位...
}
